// BASIC OPERATORS

// Multiplication
console.log(1 * 1);

// Addition
console.log(2 + 2);

// Subtraction
console.log(2 - 2);

// Division
console.log(2 / 2);

// Remainder (Modulus)
console.log(2 % 2);

// Increment
let num = 1;
num++;

console.log(num);

// Decrement

num--;

console.log(num);

// Assignment Operator (=)

// COMPARISON OPERATORS

// less than <

// greater than >

// equal to ==

console.log(3 == "3");

// equal type and equal value ===

console.log(3 === "3");

// greater than or equal to >=

console.log(3 >= 3);

// less than or equal to <=

// Not equal to !=

console.log(3 != "6");

// Not equal and equal value !==
console.log("3" !== "3");

// LOGICAL OPERATORS

let a = 3;

let b = 3;

// OR ||

console.log(a === 5 || b === 5);

// AND &&

console.log(a === 3 && b === 3);

